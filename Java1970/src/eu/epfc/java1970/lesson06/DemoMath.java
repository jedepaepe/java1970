package eu.epfc.java1970.lesson06;

public class DemoMath {

    public static void main(String[] args) {
        System.out.println("Math.abs(-10) => " + Math.abs(-10));
        System.out.println("Math.max(10, 15) => " + Math.max(10, 15));
        System.out.println("Math.floor(10.76) => " + Math.floor(10.76));
        System.out.println("Math.round(10.76) => " + Math.round(10.76));
        System.out.println("Math.ceil(10.76) => " + Math.ceil(10.76));
        System.out.println("Math.toRadians(180) => " + Math.toRadians(180));
        System.out.println("Math.toDegrees(Math.PI) => " + Math.toDegrees(Math.PI));
        System.out.println("Math.pow(10, 4) => " + Math.pow(10, 4));
        System.out.println("Math.sqrt(4) => " + Math.sqrt(4));
        System.out.println("Math.log10(10) => " + Math.log10(10));
    }
}
