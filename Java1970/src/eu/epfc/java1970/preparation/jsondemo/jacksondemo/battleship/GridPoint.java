package eu.epfc.java1970.preparation.jsondemo.jacksondemo.battleship;

/**
 *
 */
public class GridPoint {
    private int x;
    private int y;

    public GridPoint() {
    }

    
    public GridPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    
    
}
