public class CelsiusFarenheitConvertor1 {
    public static void main(String[] args) {        
        System.out.println(17 * 9 / 5);
        System.out.println("Farenheit : " + (17 * 9 / 5 + 32));
        System.out.println(17 * 9 / 5 + 32 + "Farenheit");
    }
    
}
