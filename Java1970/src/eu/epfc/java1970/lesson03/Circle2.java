package eu.epfc.java1970.lesson03;

public class Circle2 {

    public static void main(String[] args) {
        double rayon = 10;
        double perimetre = 2 * 3.14 * rayon;
        double aire = 3.14 * rayon * rayon;
        System.out.println(perimetre);
        System.out.println(aire);
    }
}
